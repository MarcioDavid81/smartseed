"use client";

import { SquarePenIcon } from "lucide-react";
import { useState } from "react";
import {
  Tooltip,
  TooltipContent,
  TooltipProvider,
  TooltipTrigger,
} from "@/components/ui/tooltip";
import { Customer } from "@/types";
import UpsertCustomerModal from "./UpsertCustomerModal";

interface Props {
  customer: Customer;
}

const EditCustomerButton = ({ customer }: Props) => {
  const [isOpen, setIsOpen] = useState(false);
  return (
    <>
      <TooltipProvider>
        <Tooltip>
          <TooltipTrigger asChild>
            <button
              onClick={() => setIsOpen(true)}
              className="hover:opacity-80 transition"
            >
              <SquarePenIcon size={20} className="text-green" />
            </button>
          </TooltipTrigger>
          <TooltipContent>
            <p>Editar</p>
          </TooltipContent>
        </Tooltip>
      </TooltipProvider>
      <UpsertCustomerModal
        isOpen={isOpen}
        onClose={() => setIsOpen(false)}
        customer={customer}
      />
    </>
  );
};

export default EditCustomerButton;
